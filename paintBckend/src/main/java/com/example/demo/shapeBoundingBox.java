package com.example.demo;

public class shapeBoundingBox {
    private int width;
    private int x;
    private int y;

    private int height;

    public void setY(int y) {
        this.y = y;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getHeight() {
        return height;
    }

    public int getY() {
        return y;
    }



    public void setWidth(int width) {
        this.width = width;
    }
}
